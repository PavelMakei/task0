package by.makei.array.exception;

public class IncorrectCustomArrayArithmeticException extends Exception {

    public IncorrectCustomArrayArithmeticException() {
    }

    public IncorrectCustomArrayArithmeticException(String message) {
        super(message);
    }

    public IncorrectCustomArrayArithmeticException(String message, Throwable cause) {
        super(message, cause);
    }

    public IncorrectCustomArrayArithmeticException(Throwable cause) {
        super(cause);
    }
}
